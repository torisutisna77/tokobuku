<h2>Prosedur Pemesanan Buku</h2>
<p>Saat ini, <strong>Toko Buku Online</strong> hanya
melayani pemesanan buku melalui WhatsApp atau Telepon.
pembayaran dilakukan dengan cara transfer ke nomor 
rekening berikut:</p>

<p>Bank: <strong>MANDIRI</strong></p>
<p>No. Rekening: <strong>1234567890</strong></p>
<p>Nama: <strong>Nama Saya</strong></p>

<p>Setelah Transfer, silakan konfirmasi dengan format
sebagai berikut:</p>

<p>
	<em><strong>KONFIRMASI [ISBN1] [JUMLAH BUKU1],
	[ISBN2] [JUMLAH BUKU2]; [JUMLAH UANG]</strong></em>
</p>

<p>Contoh: </p>
<p><strong>KONFIRMASI [888-967771-3-5] [1],
	[222-34222-1-1] [1]; [105000]</strong></p>
	
<p>Lalu kirim ke <strong>whatsApp 08123456789</strong></p>
<br /><br />

<p><em><strong>Catatan:</strong> Buku yang dipesan akan 
sampai ke rumah Anda paling lama 3 hari dari waktu
pemesanan (tergantung lokasi Anda)</em></p>

